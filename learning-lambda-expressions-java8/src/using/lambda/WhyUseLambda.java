public class WhyUseLambda {

    public static void main(String[] args) {

        System.out.println("\n Enables Functional Programming - functions as entities." +
                "\n Assigning block of code to variables - functionalInterfaces");

        System.out.println("\n Readable and concise code - simple implementations over anonymous inner class, " +
                "rather than too much overhead for simple blocks of code. " +
                "\n Like : Method References, Inline Lamda Expressions");

        System.out.println("\n Easier to use APIs and libraries - simple blocks of code can be passed " +
                "as parameters directly.\n Rather than going thru all of unnecessary code done in traditional OOP.");

        System.out.println("\n Enable support for parallel processing. " +
                "\n Like : collections.forEach() and collections.streams().");
    }
}
